public class DeclararVariables {
    public static void main(String[] args) {
        int variable1 = 27;
        double variable2 = 27.5;
        char variable3 = 'J';
        boolean variable4 = true;

        System.out.println("La variable tipo entero es: " + variable1);
        System.out.println("La variable tipo double es: " + variable2);
        System.out.println("La variable tipo char es: " + variable3);
        System.out.println("La variable tipo boolean es: " + variable4);
    }
}
